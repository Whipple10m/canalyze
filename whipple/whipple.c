#define RADTOHR  3.8197186342055
#define DEGTOMM  60.0
#define HRTOMM   60.0
#define MMTOSS   60.0

float rad_to_ddmmss(float dec)
{
  float decdeg, decmm, decss;

  decdeg = dec*RADTODEG;
  decmm = (decdeg - (int)decdeg)*DEGTOMM;
  decss = (decmm - (int)decmm)*MMTOSS;

  return (int)(decdeg)*10000.0 + (int)(decmm)*100.0 + decss;
}

float rad_to_hhmmss(float ra)
{
  float rahr, ramm, rass;

  rahr = ra*RADTOHR;
  ramm = (rahr - (int)rahr)*HRTOMM;
  rass = (ramm - (int)ramm)*MMTOSS;

  return (int)(rahr)*10000.0 + (int)(ramm)*100.0 + rass;
}

float hhmmss_to_rad(float hhmmss)
{
  int ihh, imm;
  float hh, mm, ss;

  ihh = (int)(hhmmss/10000.0);
  imm = (int)((hhmmss - ihh*10000.0)/100.0);
  ss = hhmmss - ihh*10000.0 - imm*100.0;
  hh = (float)ihh*M_PI/12.0;           /* PI radians per 12 hours */
  mm = (float)imm/60.0*M_PI/12.0;
  ss = ss/60.0/60.0*M_PI/12.0;

  return hh+mm+ss;
}

float ddmmss_to_rad(float ddmmss)
{
  int idd, imm;
  float dd, mm, ss;

  idd = (int)(ddmmss/10000.0);
  imm = (int)((ddmmss - idd*10000.0)/100.0);
  ss = ddmmss - idd*10000.0 - imm*100.0;
  dd = (float)idd*M_PI/180.0;         /* PI radians per 180 degrees */
  mm = (float)imm/60.0*M_PI/180.0;
  ss = ss/60.0/60.0*M_PI/180.0;

  return dd+mm+ss;
}
